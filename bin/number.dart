void main() {
  // int
  int year = 2022;
  int hex = 0xDEADBEEF;
  print(year);
  print(hex);

  // double
  double decimal = 1.2;
  double pi = 3.14;
  print(decimal);
  print(pi);

  double withoutDecimal = 7; // 7.0
  print(withoutDecimal);

  // num (int | double)
  num progress = 75;
  print(progress);

  progress = 80.6;
  print(progress);

  // math function
  print(3.14123.floor());
}
